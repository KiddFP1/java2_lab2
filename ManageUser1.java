    private void tblUserMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblUserMouseClicked
        int index = tblUser.getSelectedRow();
        tblModel = (DefaultTableModel) tblUser.getModel();
        txtUsername.setText(tblModel.getValueAt(index, 0).toString());
        txtPassword.setText(tblModel.getValueAt(index, 1).toString());
        String role = tblModel.getValueAt(index, 2).toString();
        if (role.equals("Admin")) {
            rdoAdmin.setSelected(true);
        } else {
            rdoAdmin.setSelected(false);
        }
        tabUser.setSelectedIndex(0);
    }//GEN-LAST:event_tblUserMouseClicked

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        isValidated();
        for (User user : list) {
            if (user.getUsername().equals(txtUsername.getText())) {
                user.setPassword(new String(txtPassword.getPassword()));
                user.setRole(rdoUser.isSelected() ? "User" : "Admin");
                fillTable();
                clearForm();
                return;
            }
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

