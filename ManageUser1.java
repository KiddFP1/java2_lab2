    private void fillTable() {
        tblModel = (DefaultTableModel) tblUser.getModel();
        tblModel.setRowCount(0);
        for (User u : list) {
            Object[] row = {u.getUsername(), u.getPassword(), u.getRole()};
            tblModel.addRow(row);
        }
    }

    void clearForm() {
        txtUsername.setText("");
        txtPassword.setText("");
        rdoUser.setSelected(true);
    }

    User readForm() {
        // Doc du lieu tu form va tao nguoi dung
        String user = txtUsername.getText();
        String pass = new String(txtPassword.getPassword());
        String role = "";
        if (rdoAdmin.isSelected()) {
            role = "Admin";
        } else {
            role = "User";
        }
        User u = new User(user, pass, role);
        return u;
    }

    boolean isValidated() {
        String user = txtUsername.getText();
        String pass = new String(txtPassword.getPassword());
        StringBuilder sb = new StringBuilder();
        if (user.equals("")) {
            sb.append("Tên người dùng không để trống!\n");
        }
        if (pass.equals("")) {
            sb.append("Mật khẩu không để trống!\n");
        }
        if (sb.length() > 0) {
            JOptionPane.showMessageDialog(this, sb.toString(), "Thông báo",
                    JOptionPane.ERROR_MESSAGE);
            return false;
        }
        return false;

    }

